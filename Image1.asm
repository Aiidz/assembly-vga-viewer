; =====================================================================
; PUTPIX MACRO
; =====================================================================
PUTPIX MACRO
    PUSH AX
    PUSH DI
    MOV  AX, DX
    MOV  DI, 320
    MUL  DI
    ADD  AX, CX
    MOV  DI, AX
    MOV  AL, COLOR
    MOV  ES:[DI], AL
    POP  DI
    POP  AX
ENDM

; =====================================================================
; ENTRY POINT
; =====================================================================
START:
    MOV  AX, @DATA
    MOV  DS, AX

    ; Copy palette to buffer (BEFORE setting ES to VGA memory)
    ; ES must equal DS for MOVSB to write into the data segment.
    PUSH DS
    POP  ES
    LEA  SI, PALETTE_DATA
    LEA  DI, PAL_BUF
    MOV  CX, 768
    REP MOVSB

    ; Switch to VGA Mode 13h
    MOV  AX, 0013h
    INT  10h
    MOV  AX, 0A000h
    MOV  ES, AX

    ; Set all palette entries to black
    MOV  DX, 03C8h
    MOV  AL, 0
    OUT  DX, AL
    MOV  DX, 03C9h
    XOR  AL, AL
    MOV  CX, 768
SET_BLACK:
    OUT  DX, AL
    LOOP SET_BLACK

    ; Fade-in: 36 steps, 1 tick (~55ms) each = ~2 seconds
    ; BH = 63 (divisor), BL = brightness
    MOV  BH, 63
    MOV  BYTE PTR STEP, 0

FADE_LOOP:
    XOR  AH, AH
    MOV  AL, STEP
    MOV  BL, 63
    MUL  BL
    MOV  BL, 35
    DIV  BL
    MOV  BL, AL

    MOV  DX, 03C8h
    MOV  AL, 0
    OUT  DX, AL
    MOV  DX, 03C9h

    LEA  SI, PAL_BUF
    MOV  CX, 256

FADE_COLOR:
    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    LOOP FADE_COLOR

    ; Wait 1 BIOS tick (save BL around tick wait)
    PUSH CX
    PUSH DX
    PUSH BX
    XOR  AH, AH
    INT  1Ah
    MOV  BX, DX
WAIT_TICK:
    XOR  AH, AH
    INT  1Ah
    CMP  DX, BX
    JE   WAIT_TICK
    POP  BX
    POP  DX
    POP  CX

    INC  BYTE PTR STEP
    CMP  BYTE PTR STEP, 35
    JLE  FADE_LOOP

    ; Clear screen
    XOR  DI, DI
    MOV  AL, 255
    MOV  CX, 64000
    REP STOSB

    ; Render grid (106 cols x 64 rows, 3x3 blocks)
    MOV  WORD PTR ROW, 0
    MOV  BX, 0

NEXTCHUNK:
    MOV  SI, BX
    SHL  SI, 1
    MOV  AX, GPTR[SI]
    MOV  GBASE, AX
    MOV  WORD PTR LROW, 0

CROWLOOP:
    MOV  WORD PTR COL, 0

CCOLLOOP:
    ; Calculate grid index: LROW * 106 + COL
    MOV  AX, LROW
    MOV  SI, 106
    MUL  SI
    ADD  AX, COL
    MOV  SI, GBASE
    ADD  SI, AX
    MOV  AL, [SI]
    MOV  COLOR, AL

    ; Screen X = COL * 3
    PUSH DX
    MOV  AX, COL
    MOV  SI, 3
    MUL  SI
    MOV  CX, AX
    POP  DX

    ; Screen Y = 4 + ROW * 3
    MOV  AX, ROW
    MOV  SI, 3
    MUL  SI
    ADD  AX, 4
    MOV  DX, AX

    ; Draw 3x3 block
    MOV  WORD PTR PROW, 0

BLKROW:
    MOV  WORD PTR PCOL, 0

BLKCOL:
    PUSH CX
    PUSH DX
    ADD  CX, PCOL
    ADD  DX, PROW
    PUTPIX
    POP  DX
    POP  CX

    INC  WORD PTR PCOL
    MOV  AX, PCOL
    CMP  AX, 3
    JL   BLKCOL

    INC  WORD PTR PROW
    MOV  AX, PROW
    CMP  AX, 3
    JL   BLKROW

    ; Small delay per cell (scanline reveal effect)
    PUSH CX
    MOV  CX, 400h
CELL_DELAY:
    LOOP CELL_DELAY
    POP  CX

    INC  WORD PTR COL
    MOV  AX, COL
    CMP  AX, 106
    JGE  NOT_CCOL
    JMP  CCOLLOOP
NOT_CCOL:

    INC  WORD PTR ROW
    INC  WORD PTR LROW
    MOV  AX, LROW
    CMP  AX, 16
    JGE  NOT_CROW
    JMP  CROWLOOP
NOT_CROW:

    INC  BX
    CMP  BX, 4
    JGE  NOT_NCHUNK
    JMP  NEXTCHUNK
NOT_NCHUNK:

    ; Wait for keypress, restore text mode, exit
    MOV  AH, 00h
    INT  16h
    MOV  AX, 0003h
    INT  10h
    MOV  AX, 4C00h
    INT  21h

END START
