; =====================================================================
; PUTPIX MACRO
;
; Draws a single pixel at screen coordinates (CX, DX) using the
; COLOR variable as the VGA palette index.
;
; Input:
;   CX = X coordinate (0-319)
;   DX = Y coordinate (0-199)
;   COLOR = palette index (0-255)
;
; Output:
;   Pixel written to VGA framebuffer at segment A000h.
; =====================================================================
PUTPIX MACRO
    PUSH AX
    PUSH DI
    MOV  AX, DX
    MOV  DI, 320
    MUL  DI
    ADD  AX, CX
    MOV  DI, AX
    MOV  AL, COLOR
    MOV  ES:[DI], AL
    POP  DI
    POP  AX
ENDM

; =====================================================================
; ENTRY POINT
; =====================================================================
START:
    MOV  AX, @DATA
    MOV  DS, AX

    ; -----------------------------------------------------------------
    ; STEP 1: COPY PALETTE TO BUFFER (BEFORE setting ES to VGA memory)
    ; We need the original palette values for fade-in scaling.
    ; ES must equal DS for MOVSB to write into the data segment.
    ; -----------------------------------------------------------------
    PUSH DS
    POP  ES
    LEA  SI, PALETTE_DATA
    LEA  DI, PAL_BUF
    MOV  CX, 768
    REP MOVSB

    ; -----------------------------------------------------------------
    ; STEP 2: SWITCH TO VGA MODE 13h
    ; 320x200 pixels, 256 colors, 1 byte per pixel.
    ; -----------------------------------------------------------------
    MOV  AX, 0013h
    INT  10h

    ; Set ES to VGA video memory segment
    MOV  AX, 0A000h
    MOV  ES, AX

    ; -----------------------------------------------------------------
    ; STEP 3: SET ALL PALETTE ENTRIES TO BLACK
    ; Screen starts black, then fades in.
    ; -----------------------------------------------------------------
    MOV  DX, 03C8h
    MOV  AL, 0
    OUT  DX, AL

    MOV  DX, 03C9h
    XOR  AL, AL
    MOV  CX, 768
SET_BLACK:
    OUT  DX, AL
    LOOP SET_BLACK

    ; -----------------------------------------------------------------
    ; STEP 4: FADE-IN ANIMATION
    ; 36 steps, 1 BIOS tick (~55ms) wait per step.
    ; Total duration: 36 * 55ms = ~2.0 seconds.
    ;
    ; For each step, scale all 256 palette entries:
    ;   brightness = STEP * 63 / 35  (maps 0..35 to 0..63)
    ;   R_out = original_R * brightness / 63
    ;   G_out = original_G * brightness / 63
    ;   B_out = original_B * brightness / 63
    ;
    ; BH = 63 (divisor, preserved across tick wait)
    ; BL = brightness (recomputed each step)
    ; -----------------------------------------------------------------
    MOV  BH, 63
    MOV  BYTE PTR STEP, 0

FADE_LOOP:
    ; Calculate brightness: STEP * 63 / 35
    XOR  AH, AH
    MOV  AL, STEP
    MOV  BL, 63
    MUL  BL
    MOV  BL, 35
    DIV  BL
    MOV  BL, AL

    ; Set palette starting index
    MOV  DX, 03C8h
    MOV  AL, 0
    OUT  DX, AL
    MOV  DX, 03C9h

    ; Scale and output all 256 palette entries
    LEA  SI, PAL_BUF
    MOV  CX, 256

FADE_COLOR:
    ; Scale R: AH=0 before LODSB so MUL BL result fits in AX
    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    ; Scale G
    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    ; Scale B
    XOR  AH, AH
    LODSB
    MUL  BL
    DIV  BH
    OUT  DX, AL

    LOOP FADE_COLOR

    ; Wait 1 BIOS tick (~55ms)
    ; Save BL (brightness) since INT 1Ah clobbers BX
    PUSH CX
    PUSH DX
    PUSH BX
    XOR  AH, AH
    INT  1Ah
    MOV  BX, DX
WAIT_TICK:
    XOR  AH, AH
    INT  1Ah
    CMP  DX, BX
    JE   WAIT_TICK
    POP  BX
    POP  DX
    POP  CX

    INC  BYTE PTR STEP
    CMP  BYTE PTR STEP, 35
    JLE  FADE_LOOP

    ; -----------------------------------------------------------------
    ; STEP 4: CLEAR SCREEN
    ; Fill the entire 64,000-byte framebuffer with color 0.
    ; -----------------------------------------------------------------
    XOR  DI, DI
    MOV  AL, 255
    MOV  CX, 64000
    REP STOSB

    ; -----------------------------------------------------------------
    ; STEP 5: RENDER GRID
    ; 4 chunks (GRID0-GRID3), each 16 rows x 106 columns.
    ; Each grid cell is rendered as a 3x3 pixel block.
    ; Grid values are direct palette indices (0-255).
    ; X = COL * 3 (fills full 320px width: 106*3=318).
    ; Y = 4 + (ROW * 3), centered vertically.
    ; -----------------------------------------------------------------
    MOV  WORD PTR ROW, 0
    MOV  BX, 0

NEXTCHUNK:
    MOV  SI, BX
    SHL  SI, 1
    MOV  AX, GPTR[SI]
    MOV  GBASE, AX
    MOV  WORD PTR LROW, 0

CROWLOOP:
    MOV  WORD PTR COL, 0

CCOLLOOP:
    ; Calculate grid index: LROW * 106 + COL
    MOV  AX, LROW
    MOV  SI, 106
    MUL  SI
    ADD  AX, COL
    MOV  SI, GBASE
    ADD  SI, AX
    MOV  AL, [SI]
    MOV  COLOR, AL

    ; Screen X = COL * 3 (protect DX)
    PUSH DX
    MOV  AX, COL
    MOV  SI, 3
    MUL  SI
    MOV  CX, AX
    POP  DX

    ; Screen Y = 4 + ROW * 3
    MOV  AX, ROW
    MOV  SI, 3
    MUL  SI
    ADD  AX, 4
    MOV  DX, AX

    ; Draw 3x3 pixel block
    MOV  WORD PTR PROW, 0

BLKROW:
    MOV  WORD PTR PCOL, 0

BLKCOL:
    PUSH CX
    PUSH DX
    ADD  CX, PCOL
    ADD  DX, PROW
    PUTPIX
    POP  DX
    POP  CX

    INC  WORD PTR PCOL
    MOV  AX, PCOL
    CMP  AX, 3
    JL   BLKCOL

    INC  WORD PTR PROW
    MOV  AX, PROW
    CMP  AX, 3
    JL   BLKROW

    ; Small delay per cell (scanline reveal effect)
    PUSH CX
    MOV  CX, 400h
CELL_DELAY:
    LOOP CELL_DELAY
    POP  CX

    ; Next column
    INC  WORD PTR COL
    MOV  AX, COL
    CMP  AX, 106
    JGE  NOT_CCOL
    JMP  CCOLLOOP
NOT_CCOL:

    ; Next row
    INC  WORD PTR ROW
    INC  WORD PTR LROW
    MOV  AX, LROW
    CMP  AX, 16
    JGE  NOT_CROW
    JMP  CROWLOOP
NOT_CROW:

    ; Next chunk
    INC  BX
    CMP  BX, 4
    JGE  NOT_NCHUNK
    JMP  NEXTCHUNK
NOT_NCHUNK:

    ; -----------------------------------------------------------------
    ; STEP 6: WAIT FOR KEYPRESS, RESTORE TEXT MODE AND EXIT
    ; -----------------------------------------------------------------
    MOV  AH, 00h
    INT  16h

    MOV  AX, 0003h
    INT  10h

    MOV  AX, 4C00h
    INT  21h

END START
